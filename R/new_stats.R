  }

parse_for_seasons_data <-
  function(json) {
    json_seasons <-
      json$data$seasons

    seasons <-
      seq_along(json_seasons) %>%
      future_map_dfr(function(x) {
        row <-
          json_seasons[[x]]

        values <-
          row[1:5] %>%
          flatten_chr()

        items <-
          c(
            "nameParameter",
            "slugLeague",
            "yearDataEnd",
            "idLeagueSeasonType",
            "yearDataStart"
          )

        tibble(items, values) %>%
          mutate(values = values %>% as.character()) %>%
          tidyr::spread(items, values)
      })

    seasons %>%
      mutate_all(as.character) %>%
      mutate_at(c("yearDataEnd", "idLeagueSeasonType", "yearDataStart"),
                funs(. %>% as.numeric()))
  }

.parse_for_teams <-
  function(json) {
    json_teams <-
      json$data$teams

    df_teams <-
      seq_along(json_teams) %>%
      future_map_dfr(function(x) {
        values <-
          json_teams[[x]] %>%
          purrr::map_chr(function(z) {
            if (z %>% length() == 0) {
              return(NA)
            }
            z %>% str_c(collapse = ", ")
          })

        items <-
          str_c("V", seq_along(values))

        tibble(items, values) %>%
          tidyr::spread(items, values)

      }) %>%
      dplyr::select(one_of(
        c("V1", "V2", "V3", "V4", "V5", "V6", "V7", "V8", "V9", "V10",
          "V11")
      )) %>%
      purrr::set_names(
        c(
          "idTeam",
          "slugTeam",
          "teamName",
          "cityTeam",
          "teamNameFull",
          "idConference",
          "idDivision",
          "isNonNBATeam",
          "yearPlayedLast",
          "idLeague",
          "colorsTeam"
        )
      ) %>%
      mutate_at(
        c(
          "idTeam",
          "idConference",
          "idDivision",
          "isNonNBATeam",
          "yearPlayedLast",
          "idLeague"
        ),
        funs(. %>% as.integer())
      ) %>%
      mutate(nameTeam = str_c(cityTeam, teamNameFull, sep = " ")) %>%
      mutate_if(is.character,
                funs(ifelse(. == "", NA, .))) %>%
      dplyr::select(nameTeam, everything())

    df_teams <-
      df_teams %>%
      mutate(
        nameTeam = nameTeam %>% str_replace_all("LA Clippers", "Los Angeles Clippers"),
        urlThumbnailTeam = if_else(
          isNonNBATeam == 0,
          glue::glue(
            "https://stats.nba.com/media/img/teams/logos/{slugTeam}_logo.svg"
          ) %>% as.character(),
          "https://stats.nba.com/media/img/teams/logos/NBA_logo.svg"
        )
      )
    df_teams

  }

#' NBA team dictionary
#'
#' Returns team dictionary
#'
#' @param join_blg if \code{TRUE} joins BLG team features data
#'
#' @return a `tibble`
#' @export
#' @importFrom readr read_lines
#' @importFrom glue glue
#' @import dplyr jsonlite stringr tidyr purrr
#' @examples
#' nba_teams()
nba_teams <-
  function(join_blg = F) {
    url <- "https://stats.nba.com/js/data/ptsd/stats_ptsd.js"
    json <-
      url %>%
      readr::read_lines() %>%
      str_replace_all("var stats_ptsd =|\\;", "") %>%
      jsonlite::fromJSON(flatten = TRUE,
                         simplifyDataFrame = TRUE)

    df_teams <-
      json %>%
      .parse_for_teams()

    if (join_blg) {
      df_teams <-
        df_teams %>%
        left_join(.blg_teams() %>% select(-one_of(c(
          "teamName", "cityTeam"
        )))) %>%
        suppressMessages()
    }
    df_teams
  }

#' NBA teams seasons
#'
#'
#'
#' @return a \code{tibble()}
#' @export
#'
#' @examples
#' nba_teams_seasons()
nba_teams_seasons <- function() {
  json <-
    "https://stats.nba.com/stats/commonteamyears/?leagueId=00" %>%
    curl_json_to_vector()
  actual_names <-
    json$resultSets$headers[[1]] %>%
    resolve_nba_names()

  data <-
    json$resultSets$rowSet[[1]] %>%
    data.frame(stringsAsFactors = F) %>%
    as_tibble() %>%
    purrr::set_names(actual_names) %>%
    munge_nba_data() %>%
    mutate(isActiveTeam = yearSeasonLast == max(yearSeasonLast)) %>%
    select(isActiveTeam, everything())
  data
}

