.get_slug_year <-
  function() {
    current_date <- Sys.Date()
    current_year <- lubridate::year(current_date)
    current_month <- lubridate::month(current_date)
    slug_year <- dplyr::case_when(current_month >= 10 ~  current_year,
                                  TRUE ~ current_year - 1)
    slug_year
  }

